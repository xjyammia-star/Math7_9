export function getModuleFromConcept(conceptId, conceptTitle) {
  const id = String(conceptId ?? '').trim().toLowerCase();
  const title = String(conceptTitle ?? '').toLowerCase();
  const haystack = `${id} ${title}`;

  if (/(pythag|ladder|triangle|circle|quadrilateral|geometry|coordinate|transformation|solid|tangent|similar|congruence|parallel|transversal|trig|angle|polygon|shape|ray|segment|midpoint|perpendicular)/.test(haystack)) return 'geometry';
  if (/(probability|statistics|data|sampling|chart|graph|mean|median|mode|variance)/.test(haystack)) return 'statistics';
  if (/(model|application|growth|decay|optim|real-world|word problem|context)/.test(haystack)) return 'modeling';
  if (/(reasoning|proof|counterexample|logic|justify|why|counterexample)/.test(haystack)) return 'reasoning';
  if (/(algebra|equation|inequality|expression|factor|fraction|decimal|percent|ratio|proportion|sequence|function|surd|index|power|root|polynomial)/.test(haystack)) return 'algebra';

  return null;
}

export function getModuleProblemTypes(moduleId) {
  return moduleProblemTypePools[moduleId] ?? [];
}

export function getConceptProblemTypeBoosters(conceptId, conceptTitle) {
  const haystack = `${String(conceptId ?? '').toLowerCase()} ${String(conceptTitle ?? '').toLowerCase()}`;
  for (const [needle, pool] of Object.entries(conceptProblemTypeBoosters)) {
    if (haystack.includes(needle)) {
      return pool;
    }
  }
  if (/(triangle)/.test(haystack)) return conceptProblemTypeBoosters.triangles;
  if (/(circle|sector|arc|tangent|chord|diameter|radius)/.test(haystack)) return conceptProblemTypeBoosters.circles;
  if (/(probability)/.test(haystack)) return conceptProblemTypeBoosters.probability;
  if (/(data|statistics|mean|median|mode|chart|graph)/.test(haystack)) return conceptProblemTypeBoosters['data-analysis'];
  if (/(pythag|ladder|distance|rectangle|square)/.test(haystack)) return conceptProblemTypeBoosters.pythagoras;
  if (/(area|perimeter|composite|shape)/.test(haystack)) return conceptProblemTypeBoosters['area-perimeter'];
  return [];
}

function expandProblemTypeVariants(pool, difficulty, conceptId, conceptTitle) {
  const normalizedDifficulty = String(difficulty ?? 'Easy').toLowerCase();
  const contextVariants = normalizedDifficulty.includes('hard')
    ? [
        'as a multi-step challenge',
        'with a hidden intermediate step',
        'in a mixed-word-problem context',
        'with a reasoning or justification twist',
      ]
    : normalizedDifficulty.includes('medium')
      ? [
          'with one extra intermediate step',
          'in a familiar but slightly altered context',
          'as a reverse problem',
          'with one extra condition to interpret',
        ]
      : [
          'as a direct textbook version',
          'with a simple concrete context',
          'with one obvious given and one obvious unknown',
          'as a basic substitution or identification task',
        ];

  const specialtyVariants = [];
  const title = `${String(conceptId ?? '').toLowerCase()} ${String(conceptTitle ?? '').toLowerCase()}`;
  if (/(geometry|triangle|circle|quadrilateral|parallel|transversal|angle|tangent|coordinate|pythag|ladder)/.test(title)) {
    specialtyVariants.push(
      'with an explicit diagram',
      'with labelled points and lengths',
      'with a proof or verification step',
      'with one hidden length or angle'
    );
  } else if (/(algebra|equation|inequality|expression|factor|fraction|decimal|percent|ratio|proportion|sequence|function|surd|index|power|root|polynomial)/.test(title)) {
    specialtyVariants.push(
      'by rewriting the expression or equation',
      'by translating words into algebra',
      'with a check by substitution',
      'with an equivalent form comparison'
    );
  } else if (/(statistics|data|sampling|chart|graph|mean|median|mode|variance|probability)/.test(title)) {
    specialtyVariants.push(
      'from a table or chart',
      'from a small data set or sample space',
      'with a comparison step',
      'with a conclusion written in context'
    );
  } else if (/(model|application|context|real-world)/.test(title)) {
    specialtyVariants.push(
      'as a realistic application',
      'with unit conversion included',
      'by comparing two scenarios',
      'with a final interpretation in context'
    );
  } else if (/(proof|reasoning|logic|counterexample|justify)/.test(title)) {
    specialtyVariants.push(
      'as a justification task',
      'with a counterexample or correction',
      'with a short proof chain',
      'with a true/false decision'
    );
  }

  const variants = [];
  for (const item of pool) {
    variants.push(item);
    for (const context of contextVariants) {
      variants.push(`${item} ${context}`);
    }
    for (const specialty of specialtyVariants) {
      variants.push(`${item} ${specialty}`);
    }
  }

  let unique = Array.from(new Set(variants));
  if (unique.length >= 30) return unique;

  const fillerShells = normalizedDifficulty.includes('hard')
    ? ['as a final challenge', 'with a hidden twist', 'with an explanation required', 'with two linked steps']
    : normalizedDifficulty.includes('medium')
      ? ['with one extra step', 'with a slight twist', 'with a comparison', 'with a reverse question']
      : ['in direct form', 'with a simple context', 'as a basic application', 'as a one-step check'];

  let counter = 1;
  for (const item of pool) {
    for (const shell of fillerShells) {
      if (unique.length >= 30) return unique;
      unique.push(`${item} ${shell} (${counter})`);
      counter += 1;
    }
  }

  while (unique.length < 30) {
    unique.push(`${pool[0] ?? 'problem type'} ${normalizedDifficulty} variant ${unique.length + 1}`);
  }

  return Array.from(new Set(unique));
}

export function getExerciseTypePool(conceptId, conceptTitle, difficulty = 'Easy', moduleId = null) {
  const normalizedDifficulty = String(difficulty ?? 'Easy');
  const fallbackPool = normalizedDifficulty.toLowerCase().includes('hard')
    ? ['multi-step modeling problem with several constraints']
    : normalizedDifficulty.toLowerCase().includes('medium')
      ? ['two-step calculation with one intermediate quantity']
      : ['direct calculation with one clear known formula'];
  const resolvedModuleId = moduleId ?? getModuleFromConcept(conceptId, conceptTitle);
  const modulePool = resolvedModuleId ? getModuleProblemTypes(resolvedModuleId) : [];
  const boosters = getConceptProblemTypeBoosters(conceptId, conceptTitle);
  const title = String(conceptTitle ?? '').toLowerCase();
  const keywordPool = [];
  for (const [key, pool] of Object.entries(conceptProblemTypeBoosters)) {
    if (title.includes(key)) keywordPool.push(...pool);
  }
  const basePool = Array.from(new Set([...keywordPool, ...modulePool, ...boosters, ...fallbackPool]));
  const expandedPool = expandProblemTypeVariants(basePool, normalizedDifficulty, conceptId, conceptTitle);
  return expandedPool.length >= 30 ? expandedPool : Array.from(new Set([...expandedPool, ...basePool]));
}
